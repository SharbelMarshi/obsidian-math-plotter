var EXPLICIT_Y_PATTERN = /^y\s*=\s*(.+)$/i;
var ODE_SLOPE_PATTERN = /^(?:dy\s*\/\s*dx|y')\s*=\s*(.+)$/i;
var EQUALS_ZERO_PATTERN = /^(.+?)\s*=\s*0\s*$/;
var GENERAL_EQUATION_PATTERN = /^(.+?)\s*=\s*(.+)$/;
function containsBothVariables(expr) {
  return /\bx\b/.test(expr) && /\by\b/.test(expr);
}
function validatePgfCompatibility(expr, mode, odeSolution = false) {
  if (/y\s*''+/i.test(expr)) {
    if (mode === "ode" && odeSolution) {
      return;
    }
    throw new GraphSyntaxError("Second-order derivatives (y'') are not supported in graph expressions.", mode === "ode" ? "Use [ode, solution]{y'' = f(x,y,y')} with x0, y0, yp0 for numerical solutions." : "Plot an explicit solution instead, e.g. \\function{y = 0.5*\\lambda*x^2}.");
  }
  if (/y\s*'(?![a-zA-Z])/i.test(expr) && mode !== "ode") {
    throw new GraphSyntaxError("Prime notation y' is not valid in pgfplots math.", "Use [ode]{dy/dx = ...} for slope fields, or give an explicit formula y = ...");
  }
  if (/\\dd(?:x|t|y)\b/i.test(expr)) {
    throw new GraphSyntaxError("Leibniz derivative notation is not supported inside plot math.", "Write dy/dx = ... in [ode] mode, or an explicit y = ... formula.");
  }
}
function normalizeFunctionExpression(raw, mode, odeSolution = false) {
  const body = raw.trim();
  if (!body) {
    throw new GraphSyntaxError("Function body is empty.", "Enter an expression such as x^2 or y = sin(x).");
  }
  validatePgfCompatibility(body, mode, odeSolution);
  const explicitY = EXPLICIT_Y_PATTERN.exec(body);
  if (explicitY && mode !== "parametric" && mode !== "polar") {
    const pgfExpr = explicitY[1].trim();
    validatePgfCompatibility(pgfExpr, mode, odeSolution);
    if (mode === "ode") {
      throw new GraphSyntaxError("ODE mode expects a slope function, not y = ...", "Use \\function[ode]{dy/dx = x + y} or \\function[ode]{x + y}.");
    }
    return { kind: mode === "pde" || mode === "3d" ? "surface" : "explicit", pgfExpr };
  }
  const odeSlope = ODE_SLOPE_PATTERN.exec(body);
  if (odeSlope) {
    const pgfExpr = odeSlope[1].trim();
    validatePgfCompatibility(pgfExpr, "ode", odeSolution);
    return { kind: "ode_slope", pgfExpr, forcedMode: "ode" };
  }
  if (odeSolution && /y\s*''/i.test(body)) {
    return { kind: "ode_slope", pgfExpr: body, forcedMode: "ode" };
  }
  const equalsZero = EQUALS_ZERO_PATTERN.exec(body);
  if (equalsZero) {
    if (mode === "ode") {
      throw new GraphSyntaxError("ODE mode expects dy/dx = f(x,y), not an equation equal to zero.", "Example: \\function[ode, domain=-3:3]{x + y}. For y'' = 0, plot y = c1*x + c2 explicitly.");
    }
    const pgfExpr = equalsZero[1].trim();
    validatePgfCompatibility(pgfExpr, mode);
    return { kind: "implicit", pgfExpr };
  }
  const equation = GENERAL_EQUATION_PATTERN.exec(body);
  if (equation) {
    const lhs = equation[1].trim();
    const rhs = equation[2].trim();
    if (mode === "ode") {
      throw new GraphSyntaxError("ODE mode expects dy/dx = f(x,y), not a general equation.", "Example: \\function[ode]{dy/dx = x - y}.");
    }
    if (EXPLICIT_Y_PATTERN.test(body)) {
      return normalizeFunctionExpression(body, mode);
    }
    if (containsBothVariables(`${lhs} ${rhs}`) || mode === "implicit") {
      const pgfExpr = `(${lhs}) - (${rhs})`;
      validatePgfCompatibility(pgfExpr, mode);
      return { kind: "implicit", pgfExpr };
    }
    throw new GraphSyntaxError(`Could not interpret equation "${body}".`, "Use y = f(x), f(x,y) = 0, or [implicit]{x^2 + y^2 = 9}.");
  }
  if (mode === "ode") {
    return { kind: "ode_slope", pgfExpr: body };
  }
  if (mode === "pde" || mode === "3d") {
    return { kind: "surface", pgfExpr: body };
  }
  if (mode === "implicit") {
    return { kind: "implicit", pgfExpr: body };
  }
  if (containsBothVariables(body)) {
    validatePgfCompatibility(body, mode);
    return { kind: "implicit", pgfExpr: body };
  }
  return { kind: "explicit", pgfExpr: body };
}
